import { useEffect, useState } from "react";
import { useRouter } from 'next/router'

import Head from "next/head";
import Image from "next/image";
import Link from "next/link";

import styles from "../styles/Home.module.scss";

import pokemon from "../samples/pokemon";

export default function search() {
	const router = useRouter();
	const [query, setQuery] = useState('');
	const [results, setResults] = useState([]);

	useEffect(() => {
		setQuery(router.query.query);
		setResults([]);
	}, [router.query]);

	useEffect(() => {
		pokemon.results.filter(character => character.name.toLowerCase().includes(query)).map(filterCharacter => (
			setResults(results => [...results, filterCharacter])
		));
	}, [query]);

    return (
        <main className={styles.homepage}>
            <Head>
                <title>{query} search results</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>
			<div className="wrapper">
				{results.length > 0 && 
					<p>{results.length} Pokémon found with &#8220;{query}.&#8221;</p>
				}
				{results &&
					results.map((result, index) => (
						<div key={index}>
							<p>{result.name}</p>
						</div>
					))
				}
				{results.length < 1 &&
					<p>Sorry, no Pokémon by that name.</p>
				}
            </div>
        </main>
    );
}
